class Solution {
  public:
    Node* constructLL(vector<int>& arr) {
        // code here
        
        Node*head=NULL,*temp;
        
        for(int i=0;i<arr.size();i++){
            
            if(!head){
                head=new Node(arr[i]);
                temp=head;
            }
            
            else{
                Node*newnode=new Node(arr[i]);
                temp->next=newnode;
                temp=temp->next;
            }
        }
        
        return head;
    }
};


